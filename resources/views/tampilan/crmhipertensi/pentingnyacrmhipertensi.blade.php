                    <p><span style="color:#000000"><em>Healthy People </em>2010<em> for Hypertension </em>menganjurkan perlunya pendekatan yang lebih komprehensif dan intensif guna mencapai pengontrolan tekanan darah secara optimal. Selain itu, <em>Joint National Committee </em>(JNC) <em>on Prevention, Detection, Evaluation and Treatment of High Blood Pressure</em> VII tahun 2003 menganjurkan modifikasi gaya hidup dalam mencegah dan menangani tekanan darah tinggi, selain terapi dengan obat. Hal ini perlu dilakukan karena apabila hipertensi tidak dideteksi secara dini dan mendapat pengobatan yang memadai, peningkatan tekanan darah yang berlangsung dalam jangka waktu lama (persisten) dapat menimbulkan kerusakan&nbsp; pada ginjal (gagal ginjal), jantung (penyakit jantung koroner) dan otak (menyebabkan <em>stroke</em>) (Pusdatin, 2014).</span></p>

                    <p><span style="color:#000000">Terapi non farmakologi seperti menjalani pola hidup sehat telah banyak terbukti dapat menurunkan tekanan darah, dan secara umum sangat menguntungkan dalam menurunkan risiko permasalahan kardiovaskular. Pada pasien yang menderita hipertensi derajat 1, tanpa faktor risiko kardiovaskular lain, maka strategi pola hidup sehat merupakan tata laksana tahap awal, yang harus dijalani setidaknya selama 4 &ndash; 6 bulan. Bila setelah jangka waktu tersebut, tidak didapatkan penurunan tekanan darah yang diharapkan atau didapatkan faktor risiko kardiovaskular yang lain, maka sangat dianjurkan untuk memulai terapi farmakologi. Beberapa pola hidup sehat yang dianjurkan oleh banyak <em>guidelines</em> adalah :</span></p>

