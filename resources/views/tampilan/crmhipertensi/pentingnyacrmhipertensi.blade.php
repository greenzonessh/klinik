                    <p style="margin-left:21.3pt"><span style="color:#000000"><span style="font-family:&quot;Times New Roman&quot;,&quot;serif&quot;">Mengganti makanan tidak sehat dengan memperbanyak asupan sayuran dan buah-buahan dapat memberikan manfaat yang lebih selain penurunan tekanan darah, seperti menghindari diabetes dan dislipidemia.</span></span></p>

